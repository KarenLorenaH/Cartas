                BufferedImage minnImagen = miImagen.getSubimage(225 * (cartica2 % 13), 315 * (cartica2 / 13), 225, 315);
           
                
                //3. Asignar la image a la etiqueta
                ImageIcon miIcono = new ImageIcon(miniImagen);
